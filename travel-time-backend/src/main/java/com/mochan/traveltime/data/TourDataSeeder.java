    // --- ДОПОМІЖНІ КЛАСИ ---
    record StopData(String cityName, String hotelName) {}
    record DayData(Integer dayNumber, List<String> events) {}

    // --- ОСНОВНИЙ МЕТОД СТВОРЕННЯ ТУРУ ---
    private void createTour(
        String title, String description, Double price, Integer totalSeats,
        String startDateStr, String endDateStr, Boolean isArchived,
        String guideEmail, String transportNumber,
        String startCityName, String startAddress,
        List<String> extraInclusions,
        List<StopData> stopsData,
        List<String> imageUrls,
        List<DayData> routeData
    ) {
        // 1. Пошук залежностей
        User guide = userRepository.findByEmail(guideEmail).orElseThrow();
        Transport transport = transportRepository.findByTransportNumber(transportNumber).orElseThrow();
        Location startLocation = locationRepository.findByCityName(startCityName).orElseThrow();

        // 2. Створення Туру
        Tour tour = Tour.builder()
            .title(title)
            .description(description)
            .price(price)
            .totalSeats(totalSeats)
            .startDate(LocalDateTime.parse(startDateStr))
            .endDate(LocalDateTime.parse(endDateStr))
            .isArchived(isArchived)
            .isHidden(false)
            .isDeleted(false)
            .guide(guide)
            .transport(transport)
            .startLocation(startLocation)
            .startAddress(startAddress)
            .images(new HashSet<>())
            .stops(new HashSet<>())
            .inclusions(new HashSet<>())
            .routeSteps(new HashSet<>())
            .build();

        // 3. Додавання картинок
        for (String url : imageUrls) {
            TourImage image = TourImage.builder().imageUrl(url).tour(tour).build();
            tour.getImages().add(image);
        }

        // 4. Додавання зупинок (Stops)
        for (StopData sd : stopsData) {
            Location loc = locationRepository.findByCityName(sd.cityName()).orElseThrow();
            Hotel hotel = hotelRepository.findByName(sd.hotelName()).orElseThrow();
            
            TourStop stop = TourStop.builder()
                .location(loc)
                .hotel(hotel)
                .tour(tour)
                .build();
            tour.getStops().add(stop);
        }

        // 5. Генерація включень (Inclusions)
        // Логіка з твого generateInclusions
        List<String> baseIncluded = List.of(
            "Проїзд комфортабельним автобусом",
            "Проживання в готелі",
            "Супровід гіда",
            "Екскурсії в запланованих туристичних об'єктах"
        );
        List<String> baseExcluded = List.of(
            "Проїзд у не влюченому в тур транспорті",
            "Вхідні квитки в місця, що не включені в тур",
            "Особисті витрати"
        );

        // Додаємо included
        for (String item : baseIncluded) {
            tour.getInclusions().add(TourInclusion.builder().itemDescription(item).isIncluded(true).tour(tour).build());
        }
        for (String item : extraInclusions) {
            tour.getInclusions().add(TourInclusion.builder().itemDescription(item).isIncluded(true).tour(tour).build());
        }
        // Додаємо excluded
        for (String item : baseExcluded) {
            tour.getInclusions().add(TourInclusion.builder().itemDescription(item).isIncluded(false).tour(tour).build());
        }

        // 6. Побудова маршруту (Route)
        for (DayData dd : routeData) {
            TourRouteStep step = TourRouteStep.builder()
                .dayNumber(dd.dayNumber())
                .tour(tour)
                .events(new HashSet<>())
                .build();
            
            for (String eventDesc : dd.events()) {
                TourRouteEvent event = TourRouteEvent.builder()
                    .description(eventDesc)
                    .tourRouteStep(step)
                    .build();
                step.getEvents().add(event);
            }
            tour.getRouteSteps().add(step);
        }

        // 7. Збереження в БД (Cascade збереже все вкладене)
        tourRepository.save(tour);
    }
